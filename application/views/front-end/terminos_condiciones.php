                                        <h2>Privacidad de los datos del usuario</h2>
                        Cuyapa maneja información sobre la ubicación actual del usuario que accede al portal para poder ofrecer un servicio personalizado de búsquedas en tiempo real y asi proporcionarle a los comerciantes la facilidad de contactar a los productores directamente y asi evitar a los intermediarios.<br> 
