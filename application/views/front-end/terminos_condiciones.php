                                        <h2>Datos que recopilamos</h2>
                                        Los datos recopilados mediante los registro del productor son sólo utilizados dentro de la platadorma de Cuyapa para poder ofrecerle un mejor servicio, solo podran ser visualizados la información que el productor permita para el conocimiento de los comercios y consumidores en busca de rubros, como tambien los coordinadores de las entidades gubernamentales verificadas para su análisis y toma de desiciones relevantes a la producción. Todos los datos son de carácter obligatorio exceptuando algunas secciones de la aplicación y en ningún momento se comparten con terceros, la información registrada será categorizada en 2 partes:
                        1-	Información general del productor: Esta será facilitada a los comercios que busquen adquirir rubros de mano directa de los productores sin pasar por intermediarios y a los coordinadores de las instituciones gubernamentales que necesiten realizar análisis.
                        
                        2-	Informacion Privada del Productor: Para darle seguridad a los productores este tipo de información será mostrada únicamente a los entes gubernamentales que necesiten hacer uso de ella. 
                        
                                        
